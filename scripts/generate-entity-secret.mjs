import crypto from 'node:crypto';
import https from 'node:https';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const PROJECT_ROOT = path.resolve(__dirname, '..');
const ENV_WALLET_PATH = path.join(PROJECT_ROOT, '.env.wallet');

// Load CIRCLE_API_KEY from .env if not set in environment
if (!process.env.CIRCLE_API_KEY) {
  const envPath = path.join(PROJECT_ROOT, '.env');
  if (fs.existsSync(envPath)) {
    const lines = fs.readFileSync(envPath, 'utf-8').split('\n');
    for (const line of lines) {
      const match = line.match(/^CIRCLE_API_KEY=(.+)$/);
      if (match) process.env.CIRCLE_API_KEY = match[1].trim();
    }
  }
}

function httpsGet(url, headers) {
  return new Promise((resolve, reject) => {
    https.get(url, { headers }, (res) => {
      let data = '';
      res.on('data', (chunk) => data += chunk);
      res.on('end', () => {
        try { resolve(JSON.parse(data)); }
        catch { resolve(data); }
      });
    }).on('error', reject);
  });
}

function generateEntitySecret() {
  return crypto.randomBytes(32).toString('hex');
}

function rsaEncrypt(publicKeyPem, secretHex) {
  const encrypted = crypto.publicEncrypt(
    {
      key: publicKeyPem,
      oaepHash: 'sha256',
      padding: crypto.constants.RSA_PKCS1_OAEP_PADDING,
    },
    Buffer.from(secretHex, 'hex')
  );
  return encrypted.toString('base64');
}

async function main() {
  const API_KEY = process.env.CIRCLE_API_KEY;
  if (!API_KEY) {
    console.error('❌ CIRCLE_API_KEY not set in environment');
    console.error('   Export it or run: CIRCLE_API_KEY=your_key node scripts/generate-entity-secret.mjs');
    process.exit(1);
  }

  console.log('=== Generate Entity Secret ===\n');

  const entitySecret = generateEntitySecret();
  console.log(`✅ Entity Secret generated: ${entitySecret}`);
  console.log(`   (${entitySecret.length} hex chars = 32 bytes)\n`);

  console.log('Fetching Circle public key...');
  const pubKeyRes = await httpsGet(
    'https://api.circle.com/v1/w3s/config/entity/publicKey',
    { Authorization: `Bearer ${API_KEY}` }
  );

  const publicKey = pubKeyRes?.data?.publicKey;
  if (!publicKey) {
    console.error('❌ Failed to fetch Circle public key');
    console.error('   Response:', JSON.stringify(pubKeyRes, null, 2));
    process.exit(1);
  }
  console.log('✅ Circle public key fetched\n');

  console.log('Encrypting entity secret with RSA-OAEP SHA-256...');
  const ciphertext = rsaEncrypt(publicKey, entitySecret);
  console.log(`✅ Entity Secret Ciphertext: ${ciphertext.slice(0, 50)}...\n`);

  const envContent = [
    '# === GENERATED BY scripts/generate-entity-secret.mjs ===',
    '# WARNING: Keep this file SECURE. Never commit to git.',
    `ENTITY_SECRET=${entitySecret}`,
    `ENTITY_SECRET_CIPHERTEXT=${ciphertext}`,
    '# === CIRCLE WALLET IDs (populated by scripts/setup-wallet.mjs) ===',
    'CIRCLE_WALLET_SET_ID=',
    'CIRCLE_WALLET_ID=',
    'CIRCLE_WALLET_ADDRESS=',
    '',
  ].join('\n');

  fs.writeFileSync(ENV_WALLET_PATH, envContent, 'utf-8');
  console.log(`✅ Saved to ${ENV_WALLET_PATH}\n`);

  console.log('========================================');
  console.log('  NEXT STEP:');
  console.log('  Run: node scripts/setup-wallet.mjs');
  console.log('========================================');
}

main().catch((err) => {
  console.error('Fatal error:', err);
  process.exit(1);
});
